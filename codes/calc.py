#Esse programa calcula a soma, a multiplicação, divisão, divisão inteira e exponenciação de um número escolhido.

n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print(f' A soma vale {s}\n O produto vale {m}\n A divisão vale {d}\n A divisão inteira vale {di}\n A exponenciação vale {e}')

